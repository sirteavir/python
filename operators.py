number1=8
number2=4

#mathematical operators
print(number1+number2)
print(number1-number2)
print(number1*number2)
print(number1/number2)
print(number1 % number2)

#comparison operators
print(f"{number1} is equal to {number2}: {number1==number2}")
print(f"{number1} is greater than {number2}: {number1> number2}")
print(f"{number1} is less than {number2}: {number1<number2}")
print(f"{number1} is greater than or equal to {number2}: {number1>=number2}")
print(f"{number1} is less than or equal to {number2}: {number1<=number2}")
print(f"{number1} is not equal to {number2}: {number1!= number2}")

# logical operators
num2=15
print(f"both statements should be true: {num2==15 and num2<20}")
print(f"one statement should be true: {num2==15 or num2>20}")

# assignment operator
num3=44
num4=34
print("num3=num4=",num3==num4)
print("num3+=num4=",num3+num4)
print("num3-=num4=",num3-num4)

